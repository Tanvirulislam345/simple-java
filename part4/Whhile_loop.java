package part4;

public class Whhile_loop {
//While loop program.
	public static void main(String[] args) {
		
		//Declare and initialing value
		int value = 0;
		
		//using While loop
		while(value < 10)
		{
			
		System.out.println("hello" + value);
		value = value + 1;
		
		/*using condition for break */
		if(value == 5)
		break;
		
		}

	}

}
